def suma():
  valor1=int(input('Ingrese el primer valor: '))
  valor2=int(input('Ingrese el segundo valor: '))
  resultado=valor1+valor2
  print(f'El resultado es: {resultado}')

def resta():
  valor1=int(input('Ingrese el primer valor: '))
  valor2=int(input('Ingrese el segundo valor: '))
  resultado=valor1-valor2
  print(f'El resultado es: {resultado}')

